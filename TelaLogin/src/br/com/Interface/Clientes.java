    private void JbAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbAtualizarActionPerformed
        //botao atualizar
        
        //pegando o dado selecionado na tabela e sustituindo.
        dm.setValueAt(Txnome.getText(),TabelaClientes.getSelectedRow(),0 );
        dm.setValueAt(Txbai.getText(),TabelaClientes.getSelectedRow(),1 );
        dm.setValueAt(Txtel.getText(),TabelaClientes.getSelectedRow(),2 );
        dm.setValueAt(Txemail.getText(),TabelaClientes.getSelectedRow(),3 );
        dm.setValueAt(Txcpf.getText(),TabelaClientes.getSelectedRow(),4 );
        dm.setValueAt(Txend.getText(),TabelaClientes.getSelectedRow(),5 );
        dm.setValueAt(Txcep.getText(),TabelaClientes.getSelectedRow(),6 );
        dm.setValueAt(Txref.getText(),TabelaClientes.getSelectedRow(),7 );
        
        //resetando campos depois de substituido os valores
  
            Txnome.setText("");         //resetando o campo para uma nova inserção 
            Txcpf.setText("");          //resetando o campo para uma nova inserção
            Txbai.setText("");          //resetando o campo para uma nova inserção
            Txtel.setText("");          //resetando o campo para uma nova inserção
            Txemail.setText("");        //resetando o campo para uma nova inserção
            Txend.setText("");          //resetando o campo para uma nova inserção
            Txcep.setText("");          //resetando o campo para uma nova inserção
            Txref.setText("");          //resetando o campo para uma nova inserção
            
        //abilitando e desabilitando botoes
        Jbsalvar.setEnabled(true);     //ativando botao salvar
        JbAtualizar.setEnabled(false); //desativando botao atualizar
        Jbexcluir.setEnabled(true);    //ativando botao excluir
        Jbver.setEnabled(true);        //ativando botao exibir
    }//GEN-LAST:event_JbAtualizarActionPerformed

