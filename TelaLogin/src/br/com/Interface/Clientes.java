     private void Populate(String nome, String bairro, String tel, String email, String cpf, String endereco, String cep, String pont){
        //adicionando os dados na linha da tabela
        
        String[] rowData={nome,bairro,tel,email,cpf,endereco,cep,pont};
        dm.addRow(rowData);
        
    }
     
     private void filter(String query){
        TableRowSorter<DefaultTableModel> tr= new TableRowSorter<>(dm);
        TabelaClientes.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }

