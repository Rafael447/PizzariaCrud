    private void JbsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbsalvarActionPerformed
        //Botão salvar
        
        if(Txnome.getText().isEmpty()){     //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);
            
        }else if(Txcpf.getText().isEmpty()){     //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);

        }else if(Txbai.getText().isEmpty()){     //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);

        }else if(Txtel.getText().isEmpty()){     //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);

        }else if(Txemail.getText().isEmpty()){       //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);
            
        }else if(Txend.getText().isEmpty()){       //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);
            
        }else if(Txcep.getText().isEmpty()){       //Verificando se o campo está preenchido
            JOptionPane.showMessageDialog(rootPane, "Campo em branco", "Alerta",JOptionPane.INFORMATION_MESSAGE);
                    
        }else{ //Se estiver preenchido
            
            //pegado os dados e enviando para a tabela
            
            Populate(Txnome.getText(), Txbai.getText(), Txtel.getText(), Txemail.getText(), Txcpf.getText(),Txend.getText(), Txcep.getText(), Txref.getText());
            
        
            Txnome.setText("");          //resetando o campo para uma nova inserção 
            Txcpf.setText("");           //resetando o campo para uma nova inserção
            Txbai.setText("");           //resetando o campo para uma nova inserção
            Txtel.setText("");           //resetando o campo para uma nova inserção
            Txemail.setText("");         //resetando o campo para uma nova inserção
            Txend.setText("");           //resetando o campo para uma nova inserção
            Txcep.setText("");           //resetando o campo para uma nova inserção
            Txref.setText("");           //resetando o campo para uma nova inserção
            
        }
  
    }//GEN-LAST:event_JbsalvarActionPerformed

